# Solicita os números como entrada do usuário
primeiro_numero = float(input("Digite o primeiro número: "))
segundo_numero = float(input("Digite o segundo número: "))

# Realiza a operação simples entre os números (adição, subtração, multiplicação e divisão)
soma = primeiro_numero + segundo_numero
subtracao = primeiro_numero - segundo_numero
multiplicacao = primeiro_numero * segundo_numero
divisao = primeiro_numero / segundo_numero

# Imprime os resultados das operações
print("Resultado da soma:", soma)
print("Resultado da subtração:", subtracao)
print("Resultado da multiplicação:", multiplicacao)
print("Resultado da divisão:", divisao)
